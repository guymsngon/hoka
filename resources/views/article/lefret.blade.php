                <p>Grâce à la dynamique du commerce mondial, les flux de transport se multiplient et s’internationalisent.
                    Le transport de fret est en plein essor et il est un rouage essentiel et incontournable de l’économie moderne. </p>

                <div>
                    <h3>Définition du transport de Fret</h3>
                </div>
                <div>
                    <p>Le transport de fret ou transport de marchandises désigne le fait de déplacer
                        d’une zone à une autre des éléments commerciaux en utilisant divers modes de transport et diverses infrastructures.</p>
                </div>
                <div>
                    <img src="" style="height: 400px; width: 400px">
                    <p>Ce secteur très dynamique s’insère en effet dans un ensemble complexe d’infrastructures.
                        Le transport de fret ne peut se manifester que s’il existe des voies de communication (routes, voies ferrées…),
                        des aménagements pour transférer les marchandises d’un mode de transport à un autre, stocker les véhicules et
                        héberger les conducteurs (port, aéroport, parkings…) et enfin des ouvrages
                        d’art nécessaires pour franchir les obstacles naturels (ponts, tunnels etc…). </p>
                    <p>On compte deux types de de transport de fret: </p>
                    <ul>
                        <li>-   le transport pour compte propre </li>
                        <li>-	le transport pour compte d’autrui. </li>
                    </ul>
                    <p>Le transport pour compte propre désigne le transport de ses propres marchandises ou de marchandises dont on a la responsabilité
                        par des moyens appartenant à son entreprise.(véhicule et conducteur)</p>
                    <p>Le transport pour compte d’autrui ou transport public désigne quant à lui le transport par ses propres moyens ou par des moyens sous traités,
                        de la marchandise appartenant à autrui.</p>
                </div>
                <div>
                    <h3>Quels sont les différents modes de transport ? </h3>
                    <p>De nombreux modes de transport de fret existent. Ainsi, le fret est maritime, aérien, routier, fluvial ou ferroviaire. </p>
                    <p>Le mode de transport choisi peut répondre à différents impératifs liés au transport. Cela peut être un délai à respecter,
                        une distance à parcourir ou une limite d’émission de CO2 à ne pas dépasser. Ce dernier point notamment est un élément fondamental,
                        car pour le transport international, le développement durable est un réel enjeu.
                        Ainsi choisir tel ou tel mode de transport est avant tout une réponse à une problématique spécifique.</p>
                    <p>Par conséquent, chaque mode possède ses forces et ses faiblesses.</p>
                    <ul>
                        <li>-	Le transport routier est flexible mais limité en distance de parcours. </li>
                        <li>-	Le transport ferroviaire est rapide et fiable, mais il est moins rapide que le transport aérien et ne permet pas de d’atteindre certaines destinations.  </li>
                        <li>-	Le transport aérien quant à lui, bien que rapide, se révèle coûteux et gros émetteur de CO2</li>
                    </ul>
                    <p>Aucun mode de transport ne possède donc tous les avantages. C’est la raison pour laquelle, il existe le transport multimodal qui est
                        la combinaison de ces différentes solutions. </p>
                    <p>Dans tous les cas, le transport routier de marchandises reste un des fondements de la logistique. En effet, pour emmener les marchandises
                        vers un avion ou un port, il est nécessaire de le transporter auparavant via la route.</p>
                    <img src="" style="height: 200px; width: 500px">
                    <div>
                        <h3>Quelle réglementation sur le transport de fret ? </h3>
                        <p>La complexité du transport de marchandises requiert une législation couvrant tous les aspects du fret. Cette législation peut être très complexe.
                            Par conséquent, pour pouvoir exercer son activité en toute légalité, il est nécessaire de bien se renseigner avant d’initier un transport.</p>
                        <p>Le contrat de transport de marchandises</p>
                        <p>Pour encadrer le transport de fret, on met en place un contrat de transport de marchandises. Entre les principaux acteurs, à savoir l’expéditeur (aussi appelé chargeur),
                            le transporteur et le destinataire. Ce contrat est matérialisé par différents documents selon le mode de transport choisi</p>
                        <p>La lettre de voiture pour le transport routier et ferroviaire</p>
                        <p>La LTA ou Lettre de transport aérien pour le transport aérien</p>
                        <p>Le connaissement maritime pour le transport maritime.</p>
                        <p>Ces documents permettent de consigner toutes les particularités du transport en question. Ainsi, la description de la marchandise, les tarifs pratiqués ainsi que les délais et
                            lieux de livraisons y sont inscrits. Ils lient les différentes parties prenantes et sa présence est obligatoire lors du transport. </p>
                    </div>
                    <div>
                        <h3>Quels sont les intervenants ? </h3>
                        <p>Le transport de fret est un processus complexe. Pour cette raison, les acteurs sont multiples et indispensables au bon déroulement du transport.</p>
                        <p>•	Le commissionnaire de transport </p>
                        <p>Le rôle d’un commissionnaire de transport est d’organiser le transport des marchandises avec les transporteurs en échange d’une commission. Pour ce faire, il choisit les modes
                            de transport qui lui semblent les plus adaptés. L’intérêt est de gagner du temps et de déléguer à un professionnel si on pense ne pas maîtriser tous les rouages du fret international.</p>
                        <p>Les matières premières en négoce international sont multiples. Par conséquent, certaines exigent des spécificités lors du transport. Dans ce genre de situation, déléguer le transport peut être une solution pertinente.</p>
                        <p>•	Le transitaire</p>
                        <p>Le transitaire est celui mandaté par un donneur d’ordre (expéditeur ou destinataire) pour organiser la liaison entre les différents transporteurs dans le cas de marchandises devant emprunter plusieurs transports successifs.
                            Son rôle est d’assurer la continuité du transport. Il doit également effectuer toutes les opérations administratives s’y rapportant, telles que les formalités en douane par exemple</p>
                        <img src="" style="height: 200px;width: 500px">
                        <p>Contrairement au commissionnaire, ce n’est pas lui qui détermine le plan de transport, c’est lui qui en assure le bon déroulement.</p>
                        <p>•	Les intermédiaires: courtiers ou mandataires client</p>
                        <p>Le mandataire représente le client auprès du transporteur ou du commissionnaire de transport pour établir un ordre de transport de marchandises. Certains intermédiaires opèrent directement sur Internet grâce au développement
                            de la création d’ordres de transport en ligne.</p>

                    </div>
                    <div>
                        <h3>Quels sont les différents documents de transport nécessaires ? </h3>
                        <p>Ainsi, qu’il s’agisse de transport aérien, transport routier, transport fluvial ou transport ferroviaire, les acteurs sont très nombreux. Le monde de la logistique internationale est d’une rare complexité. En effet, on compte des agents,
                            des experts, des ouvriers en grand nombre. De plus, ces acteurs sont actifs dans des domaines variés : énergie, matières premières, négoce, industrie et services internationaux divers.</p>
                        <p>Pour cette raison, un certain nombre de documents sont nécessaires lors du transport. Cependant, ils diffèrent selon le mode de transport utilisé. </p>
                        <p>Pour le transport routier, il existe la lettre de voiture dont nous avons déjà parlé. Mais d’autres documents sont nécessaires tels que la déclaration de chargement, un protocole de sécurité pour le chargement et le déchargement ainsi que
                            des consignes de sécurité. Sans oublier les documents douaniers en cas de sortie de territoire.</p>
                        <p>Quel que soit le mode de transport, les documents doivent justifier de la nature de la cargaison et de l’existence d’une commande.</p>
                        <p>Chacun y trouve son compte. En effet, le donneur d’ordre peut faire jouer la concurrence tandis que le transporteur évite des voyages à vide. La bourse de fret lui permet ainsi d’optimiser au maximum ses trajets. Malgré le coût,
                            les opportunités sont réelles pour les transporteurs.</p>
                        <p>Pour les gros transports de marchandises, la bourse de fret peut donc être une solution. En ce qui concerne les transports plus restreints (inférieurs à 3 tonnes), il est possible de passer par un service de transport express ou messagerie logistique.
                            Des entreprises telles que DHL ou Fedex ont en effet mis en place des offres de transport rapides de colis groupés. La messagerie express est donc pertinente pour les petits envois et vous évite de passer par une bourse de fret. </p>
                        <p>Enfin, il est également possible de passer par un commissionnaire de transport ou directement par un transitaire ou un transporteur pour organiser son transport de fret.</p>
                        <p>Pour conclure, le transport de fret nécessite avant tout de bien délimiter son périmètre d’activité pour choisir les prestataires et les partenaires adéquats. Chaque transport et chaque entreprise possède ses particularités et ses problématiques spécifiques.
                            Cela peut sembler complexe au premier abord, mais en usant des bons outils et en mettant en place des partenariats adaptés.</p>

