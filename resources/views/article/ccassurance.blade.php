                    <h3>Quel est l’intérêt de la souscription à une police d’assurance transport ?</h3>
                    <p>Indemnisation par le commissionnaire de transport ou le transporteur : Après la survenance d’un risque, votre commissionnaire de transport ou votre transporteur
                        pourra bien évidemment vous indemniser conformément aux dispositions des conventions régissant chaque mode de transport. Toutefois, notez bien que ces indemnités
                        sont généralement plafonnées en fonction du poids de la marchandise. De plus, votre transporteur pourra vous indemniser si et seulement s’il n’a pas pu s’exonérer
                        de sa responsabilité de plein droit, en fonction de la valeur déclarée de votre marchandise.</p>
                    <p>Indemnisation par l’assureur : Pour être indemnisé par un assureur, vous devez au préalable souscrire une police d’assurance auprès de ce dernier en faisant bien
                        attention aux risques couverts et aux risques exclus par cette police. Car en cas de préjudice, l’indemnisation se fera uniquement en fonction des risques garantis,
                        et l’indemnisation sera calculée sur la base de la valeur assurée de votre marchandise.</p>
                    <p>Lorsque votre commissionnaire de transport ou votre transporteur sont exonérés de toute responsabilité, l’assurance transport vous permet d’obtenir une indemnisation égale au préjudice subi.</p>
                    <p>Remarque : il ne faut donc pas confondre l’indemnité versée par un transporteur fautif et l’indemnité versée par l’assurance transport.</p>
