                    <h3>La notion d’avarie</h3>
                    <p>Les pertes et avaries particulières concernent tous les modes de transport. Ce sont les détériorations ou manquants sur la marchandise elle-même, qui proviennent soit d’événements majeurs survenant
                        au cours du transport (naufrage, accident de la route, déraillement, …etc.) soit d’événements concernant uniquement la marchandise (chute, perte, …etc.).</p>
                    <p>L’avarie commune est une notion spécifique au transport maritime et fluvial. Ce terme désigne les dommages résultant de décisions prises pour « sauver le navire » et donnant lieu à contribution :
                        les frais et les sacrifices engagés sont répartis entre l’armateur et le(s) propriétaire(s) de la cargaison, proportionnellement à la valeur de chaque expédition. Ainsi, une marchandise intacte mais
                        sauvée par un acte de remorquage, contribuera aux dépenses. Les sommes mises en jeu sont considérables et font de l’avarie commune un risque majeur, qu’il est indispensable d’assurer.</p>

                </div>
