                    </div>
                    <div class="row">
                        <div class="col-lg-6"><p>Baisse faculté visuelle, auditive, …etc.</p></div>
                        <div class="col-lg-6"><p>Porter les corrections appropriées</p></div>
                    </div>
                    <div class="row">
                        <div class="col-lg-6"><p>Maladies et prise de médicaments</p></div>
                        <div class="col-lg-6"><p>Connaître les contre-indications à la conduite</p></div>
