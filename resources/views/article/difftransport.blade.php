                </div>
                <div>
                    <h3>5.     Rappels pour les employeurs</h3>
                </div>
                <div>
                    <p>L’employeur doit prendre les mesures nécessaires pour assurer la sécurité et protéger
                        la santé physique et mentale des travailleurs. Ces mesures comprennent : des actions de prévention, d’information,
                        ainsi que la mise en place d’une organisation et des moyens adaptés.</p>
                    <p>L’employeur doit évaluer les risques pour la santé et la sécurité des conducteurs et les mettre à jour les résultats
                        de cette évaluation dans un document unique.</p>
                    <p>De manière générale, pour une plus grande efficacité, les conducteurs ou leur représentant doivent :</p>
                    <p>Être consulté avant toute décision d’aménagement important modifiant les conditions de santé, de sécurité ou les conditions
                        de travail des conducteurs ;</p>
                    <p>Être associés sur les actions de formations à la sécurité ;</p>
                    <p>Avoir accès au document unique d’évaluation des risques.</p>
                    <p>Le mauvais état de nos routes n’est pas une fatalité ; les accidents peuvent toujours être évités.</p>
