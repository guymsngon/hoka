                    <p>Accidents de la route, accidents liés à la manutention lors du chargement ou du déchargement du véhicule,
                        accidents liés au bâchage ou au dé-bâchage, accidents liés à la chute ou à la descente de la cabine du véhicule,
                        accidents liés aux troubles dorso-lombaires provenant des vibrations du véhicule et à la station assise prolongée,
                        accidents liés aux affections psychosomatiques et au stress des contraintes de temps, de sécurité et de possibilité de vols,
                        accidents liés aux impacts des inhalations d’hydrocarbures, …etc., les conducteurs routiers sont en permanence exposés à de
                        nombreux risques professionnels lors du transport des personnes et des marchandises. </p>
                    <img src="" height="300px" width="500px">
                    <p>Les entreprises de transport routier de marchandises et de personnes sont ainsi marquées par une sinistralité importante.
                        Mais loin d’être une fatalité, l’exposition aux risques existants peut être réduite grâce à la mise en place de mesures de prévention efficace.</p>
                    <p>Dans cet article, nous vous proposons quelques mesures à mettre en place afin de réduire de manière non négligeable, les risques d’accidents sur nos routes.</p>

                </div>
                <div>
                    <h3>1.     Quels sont les principaux risques encourus dans le transport routier ?</h3>
                </div>
                <di>
                    <p>Les risques du transport routier peuvent être catégorisés de diverses manières, parmi lesquelles nous pouvons avoir :</p>
                    <p>Risques physiques (accidents de la route de toute nature, manutentions manuelles lors des opérations de chargement et de déchargement, travail en hauteur, postures statiques prolongées, vibrations, …etc.) ;</p>
                    <p>Risques sensoriels (bruits intérieur et extérieur de la cabine), astreinte visuelle, exposition au froid pour les camions frigorifiques, …etc.) ;</p>
                    <p>Risques cancérigènes, mutagènes, repro-toxiques et biologiques (contact avec les produits transportés tels que les ordures ménagères, lors des traversées de certaines zones, …etc.) ;</p>
                    <p>Risques chimiques (gaz d’échappement, pollution atmosphérique, nature des produits transportés, …etc.) ;</p>
                    <p>Charge mentale (prise d’informations permanente, vigilance continue, contraintes psychologiques sur les délais par exemple, …etc.) ;</p>
                    <p>Risques organisationnels (travail de nuit, horaires irréguliers, manger et dormir dans le camion, travail isolé, …etc.) ;</p>
                    <p>Autres risques (agressions et autres).</p>
                </di>
                <div>
                    <h3>2.     Comment classer les risques dans le transport routier ?</h3>
                </div>
                <div>
                    <p>Ces risques contribuent, chacun en ce qui le concerne, à rendre nos routes encore plus accidentogènes chaque jour. Toutefois, ils peuvent néanmoins être classés par la combinaison de deux critères principaux qui sont
                        la gravité et la fréquence.</p>
                    <p>Gravité	Faible:	     Sans arrêt de travail</p>
                    <p>Peu grave:	      Arrêt de travail de moins de trois jours</p>
                    <p>Grave	    :                 Arrêt de travail supérieur à trois jours</p>
                    <p>Très grave:	       Lésions irréversibles, maladies incurables, décès</p>
                    <p>Fréquence:	       Rare	Une fois par an</p>
                    <p>Peu souvent:	        Une fois par mois</p>
                    <p>Souvent       :	        Une fois par semaine</p>
                    <p>Très souvent:	       Au moins une fois par jour</p>
                </div>
                <div>
                    <h3>3.     Quelques bonnes pratiques à mettre en place</h3>
                </div>
                <div>
                    <p>Afin d’éviter certains risques, certaines bonnes pratiques peuvent être mises en place, indépendamment du mauvais état général de nos routes.</p>
                    <p>C’est ainsi que nous aurons par exemple :</p>
                    <p>Mesures sur le plan technique	</p>
                    <p>·        Choix de l’ergonomie du véhicule (cabine suspendue, siège suspendu, accessibilité de la cabine, chauffage, climatisation, …etc.) ;</p>
                    <p>·        Entretien régulier du véhicule, carnet de route ;</p>
                    <p>·        Équipements de protection collective (dessus de citerne, quai, fosse, …etc. ;</p>
                    <p>·        Équipements de protection individuelle (EPI) en cas d’arrêt sur la route et en fonction de certaines activités (casque, gants, …etc. ;</p>
                    <p>·        …etc.</p>
                    <p>Mesures sur le plan organisationnel</p>
                    <p>·        Fiche de route tenant compte du respect de la réglementation des temps de conduite et des temps de repos ;</p>
                    <p>·        Choix des itinéraires, des heures de circulation ;</p>
                    <p>·        Réévaluer les plannings en cas de surcharge de travail due aux attentes chez le(s) client(s), itinéraire dévié, …etc. ;</p>
                    <p>·        …etc.</p>

                </div>
                <div>
                    <h3>4.     Et sur le plan personnel, que peut faire le chauffeur ?</h3>
                </div>
                <div>
                    <p>La particularité de la route est qu’il y a les risques provoqués par les autres, mais aussi ceux engendrés par notre propre comportement au volant.</p>
                </div>
                <div>
                    <div class="row">
                        <div class="col-lg-6"><p>Face à certaines habitudes :</p></div>
                        <div class="col-lg-6"><p>S’efforcer de :</p></div>
                    </div>
                    <div class="row">
                        <div class="col-lg-6"><p>Usage du téléphone portable au volant</p></div>
                        <div class="col-lg-6"><p>Éviter de téléphoner en conduisant</p></div>
                    </div>
                    <div class="row">
                        <div class="col-lg-6"><p>Braver les intempéries et autres	Informer</p></div>
